// Recorded-Ajv-error fixture generator (P08, phase task 5).
//
// Runs real Ajv 2020-12 validation (the exact §B.1 config P09 will ship) over
// targeted rows of the committed fixtures and snapshots the raw error arrays
// to tests/fixtures/synthetic/ajv-errors/*.errors.json, so the P08 translator
// tests exercise genuine Ajv output without depending on Ajv at test time.
// Standalone by design — NOT part of generate-fixtures.mjs / fixtures:check;
// re-run manually (`node scripts/record-ajv-errors.mjs`) only when the HESP or
// mini schemas change, and commit the diff.
//
// Row shaping mimics json-schema-subsystem.md §C.3: SQL-NULL cells (JSON null)
// are deleted (property absent), and dataset columns outside the schema's
// property universe ('notes') are stripped — except in the dedicated
// unevaluatedProperties scenarios, which exercise the translator's fallback
// dedupe path. Recorded errors keep {keyword, instancePath, schemaPath,
// params, message} plus `data` for cell-level errors only (row-level `data`
// would embed the whole 265-column row; the translator never reads it).

import { mkdirSync, readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { Ajv2020 } from 'ajv/dist/2020.js';

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(SCRIPT_DIR, '..');
const FIXTURES = join(REPO_ROOT, 'tests', 'fixtures');
const OUT_DIR = join(FIXTURES, 'synthetic', 'ajv-errors');

/** @param {string} path */
function readJson(path) {
  return JSON.parse(readFileSync(path, 'utf8'));
}

function buildAjv() {
  // §B.1 config (minus ajv-formats: HESP/mini use no `format`, and P09 owns it).
  return new Ajv2020({ allErrors: true, verbose: true, strict: false, validateSchema: true });
}

/**
 * Register every HESP schema file under its quac-set:/ retrieval URI (§B.2)
 * and compile the root's items pointer (§B.3).
 */
function compileHesp() {
  const dir = join(FIXTURES, 'hesp', 'json_schema');
  const ajv = buildAjv();
  /** @param {string} rel */
  const walk = (rel) => {
    for (const entry of readdirSync(join(dir, rel), { withFileTypes: true })) {
      const childRel = rel === '' ? entry.name : `${rel}/${entry.name}`;
      if (entry.isDirectory()) walk(childRel);
      else if (entry.name.endsWith('.json') && entry.name !== 'manifest.json') {
        ajv.addSchema(readJson(join(dir, childRel)), `quac-set:/${childRel}`);
      }
    }
  };
  walk('');
  const validate = ajv.getSchema('https://schemas.example.org/hesp/core/core.schema.json#/items');
  if (!validate) throw new Error('HESP items pointer failed to compile');
  return validate;
}

function compileMini() {
  const ajv = buildAjv();
  const schema = readJson(join(FIXTURES, 'synthetic', 'mini', 'mini.schema.json'));
  ajv.addSchema(schema, 'quac-set:/mini.schema.json');
  const validate = ajv.getSchema(`${schema.$id}#/items`);
  if (!validate) throw new Error('mini items pointer failed to compile');
  return validate;
}

/**
 * §C.3 shaping: drop null-valued keys (absent) and, unless `keepExtras`,
 * every key outside the schema property universe.
 * @param {Record<string, unknown>} row
 * @param {{ keepExtras?: boolean; extras?: string[] }} [opts]
 */
function shapeRow(row, opts = {}) {
  const extras = new Set(opts.extras ?? ['notes']);
  /** @type {Record<string, unknown>} */
  const shaped = {};
  for (const [key, value] of Object.entries(row)) {
    if (value === null) continue;
    if (!opts.keepExtras && extras.has(key)) continue;
    shaped[key] = value;
  }
  return shaped;
}

/**
 * @param {import('ajv').ValidateFunction} validate
 * @param {Record<string, unknown>} row
 */
function collectErrors(validate, row) {
  validate(row);
  return (validate.errors ?? []).map((e) => ({
    keyword: e.keyword,
    instancePath: e.instancePath,
    schemaPath: e.schemaPath,
    params: e.params,
    message: e.message,
    // Cell-level data only; row-level data would embed the entire row.
    ...(e.instancePath === '' ? {} : { data: e.data }),
  }));
}

/**
 * @param {string} name
 * @param {{ scenario: string; description: string; row: number;
 *           errors: ReturnType<typeof collectErrors> }[]} scenarios
 */
function writeFixture(name, scenarios) {
  const payload = {
    $comment:
      'Recorded Ajv error arrays for P08 translator tests. Generated by ' +
      'scripts/record-ajv-errors.mjs (re-run manually when schemas change); ' +
      'rows shaped per json-schema-subsystem.md §C.3.',
    ajv: '8.x (Ajv2020, allErrors+verbose+strict:false)',
    scenarios,
  };
  writeFileSync(join(OUT_DIR, name), `${JSON.stringify(payload, null, 2)}\n`, 'utf8');
  console.log(`wrote ${name}: ${String(scenarios.length)} scenarios`);
}

// ---------------------------------------------------------------------------
// mini — every dirty row of mini_invalid.csv, typed by hand (§G fixture 1)
// ---------------------------------------------------------------------------

function recordMini() {
  const validate = compileMini();
  /** @type {{ scenario: string; description: string; row: number; data: Record<string, unknown>; keepExtras?: boolean }[]} */
  const rows = [
    { scenario: 'pattern', description: "row 1: id 'X99' breaks ^R[0-9]{3}$", row: 1, data: { id: 'X99', age: 30, score: 0.75, consent: 1 } },
    { scenario: 'anyof-range', description: 'row 2: age 150 above the numeric branch max', row: 2, data: { id: 'R102', age: 150, score: 0.5, consent: 1 } },
    { scenario: 'anyof-range-number', description: 'row 4: score 1.5 above max in a number column', row: 4, data: { id: 'R104', age: 40, score: 1.5, consent: 1 } },
    { scenario: 'oneof-codes', description: 'row 5: consent 5 outside the code set', row: 5, data: { id: 'R105', age: 50, score: 0.5, consent: 5 } },
    { scenario: 'conditional-const', description: 'row 6: consent 0 but score 0.5 (allOf[0] then.const -777)', row: 6, data: { id: 'R106', age: 60, score: 0.5, consent: 0 } },
    { scenario: 'required-missing-cell', description: 'row 7: empty age cell → property absent', row: 7, data: { id: 'R107', score: 0.75, consent: 1 } },
    { scenario: 'cast-failure-absent', description: "row 3: age 'abc' failed the cast → NULL → absent (translator must stay silent via castFailures)", row: 3, data: { id: 'R103', score: 0.25, consent: 1 } },
    { scenario: 'unevaluated-extra-column', description: "row 0: clean values but the 'notes' column retained (fallback path)", row: 0, data: { id: 'R101', age: 25, score: 0.5, consent: 1, notes: 'ok' }, keepExtras: true },
  ];
  writeFixture(
    'mini.errors.json',
    rows.map(({ scenario, description, row, data, keepExtras }) => ({
      scenario,
      description,
      row,
      errors: collectErrors(validate, shapeRow(data, { keepExtras: keepExtras ?? false })),
    })),
  );
}

// ---------------------------------------------------------------------------
// hesp — seeded dirty rows + targeted mutations of verified-clean rows
// ---------------------------------------------------------------------------

function recordHesp() {
  const validate = compileHesp();
  /** @type {Record<string, unknown>[]} */
  const dirtyRows = readJson(join(FIXTURES, 'hesp', 'data', 'hesp_dirty_100.json'));
  const manifest = readJson(join(FIXTURES, 'hesp', 'data', 'seeded-violations.json'));

  /** @type {Set<number>} */
  const dirtyRowIdx = new Set();
  for (const injection of manifest.injections) {
    for (const r of injection.rows) dirtyRowIdx.add(r);
  }

  /**
   * First clean row (not touched by any injection, valid post-shaping)
   * matching `predicate`.
   * @param {(row: Record<string, unknown>) => boolean} predicate
   * @param {string} label
   */
  const cleanRow = (predicate, label) => {
    for (let i = 0; i < 100; i++) {
      if (dirtyRowIdx.has(i)) continue;
      const raw = dirtyRows[i];
      if (raw === undefined || !predicate(raw)) continue;
      const shaped = shapeRow(raw);
      if (validate(shaped)) return { index: i, shaped };
    }
    throw new Error(`no clean base row found for: ${label}`);
  };

  /** @param {{ kind: string }} target */
  const injectionRow = (target) => {
    const injection = manifest.injections.find((/** @type {{ kind: string }} */ i) => i.kind === target.kind);
    if (!injection) throw new Error(`injection kind not found: ${target.kind}`);
    const idx = injection.rows[0];
    const raw = dirtyRows[idx];
    if (raw === undefined) throw new Error(`dirty row ${String(idx)} missing`);
    return { index: idx, shaped: shapeRow(raw) };
  };

  const scenarios = [];

  // Seeded dirty rows (cross-checked against seeded-violations.json).
  const seeded = [
    { scenario: 'pattern-break', kind: 'pattern-break', description: "row 10: record_id 'HH1234_W01' (golden #5)" },
    { scenario: 'range-break', kind: 'range-break', description: 'row 0: reference_year 2150 above max' },
    { scenario: 'sentinel-in-numeric-branch', kind: 'sentinel-in-numeric-branch', description: 'row 1: wage_income_annual -555 (V15 injection)' },
    { scenario: 'ifthen-const-break', kind: 'ifthen-const-break', description: 'row 2: baseline_record 1 but move_reason 3 (golden #3, cond 12)' },
    { scenario: 'ifthen-notconst-break', kind: 'ifthen-notconst-break', description: 'row 25: moved_since_last_wave 1 but move_reason -666 (golden #4, cond 14)' },
  ];
  for (const { scenario, kind, description } of seeded) {
    const { index, shaped } = injectionRow({ kind });
    scenarios.push({ scenario, description, row: index, errors: collectErrors(validate, shaped) });
  }

  // Targeted mutations of clean rows (goldens 1, 2, 6, 7 + coexistence).
  const substantive = (/** @type {unknown} */ v) => typeof v === 'number' && v >= 0;
  const wageBase = cleanRow((r) => substantive(r.wage_income_annual), 'wage golden');
  scenarios.push({
    scenario: 'wage-above-max',
    description: 'clean row, wage_income_annual := 75000000 (golden #1)',
    row: wageBase.index,
    errors: collectErrors(validate, { ...wageBase.shaped, wage_income_annual: 75000000 }),
  });

  const selfempBase = cleanRow((r) => substantive(r.selfemp_income_annual), 'selfemp golden');
  scenarios.push({
    scenario: 'selfemp-below-min',
    description: 'clean row, selfemp_income_annual := -6000000 (golden #2 replacement per V15)',
    row: selfempBase.index,
    errors: collectErrors(validate, { ...selfempBase.shaped, selfemp_income_annual: -6000000 }),
  });

  // No fixture row is a split-off household; make one (panel_membership_status
  // 3 satisfies cond 179's not-NA and nothing else keys on or targets it).
  const splitBase = cleanRow(() => true, 'split-off household golden');
  scenarios.push({
    scenario: 'split-origin-malformed',
    description: "clean row made split-off (panel_membership_status := 3), split_origin_household_id := 'HH12' (golden #6)",
    row: splitBase.index,
    errors: collectErrors(validate, {
      ...splitBase.shaped,
      panel_membership_status: 3,
      split_origin_household_id: 'HH12',
    }),
  });

  const partnerBase = cleanRow((r) => typeof r.partner_age === 'number', 'partner_age golden');
  const partnerRow = { ...partnerBase.shaped };
  delete partnerRow.partner_age;
  scenarios.push({
    scenario: 'required-missing-cell',
    description: 'clean row with substantive partner_age, then the cell blanked (golden #7)',
    row: partnerBase.index,
    errors: collectErrors(validate, partnerRow),
  });

  const baselineBase = cleanRow((r) => r.baseline_record === 1, 'coexistence base');
  scenarios.push({
    scenario: 'coexistence-value-and-conditional',
    description: 'baseline row, move_reason := -555 — fails the oneOf AND cond 12 on one cell',
    row: baselineBase.index,
    errors: collectErrors(validate, { ...baselineBase.shaped, move_reason: -555 }),
  });

  const unevalBase = cleanRow(() => true, 'unevaluatedProperties fallback');
  scenarios.push({
    scenario: 'unevaluated-extra-column',
    description: "clean row with the dirty dataset's 'notes' column retained (fallback path)",
    row: unevalBase.index,
    errors: collectErrors(validate, { ...unevalBase.shaped, notes: 'lorem' }),
  });

  writeFixture('hesp.errors.json', scenarios);
}

// ---------------------------------------------------------------------------
// oneOf multi-match — synthetic (no HESP/mini value can match two branches)
// ---------------------------------------------------------------------------

function recordOneOfMultiMatch() {
  const ajv = buildAjv();
  const schema = {
    $id: 'quac-set:/multi.schema.json',
    type: 'array',
    items: {
      type: 'object',
      properties: { x: { oneOf: [{ type: 'integer' }, { const: 5 }] } },
      required: ['x'],
    },
  };
  ajv.addSchema(schema);
  const validate = ajv.getSchema(`${schema.$id}#/items`);
  if (!validate) throw new Error('multi-match items pointer failed to compile');
  writeFixture('oneof-multimatch.errors.json', [
    {
      scenario: 'oneof-multi-match',
      description: 'x = 5 satisfies both branches → params.passingSchemas [0, 1]',
      row: 0,
      errors: collectErrors(validate, { x: 5 }),
    },
  ]);
}

mkdirSync(OUT_DIR, { recursive: true });
recordMini();
recordHesp();
recordOneOfMultiMatch();
console.log('done');

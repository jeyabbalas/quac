export const sqlLiteral = (v: string | number | null): string =>
  v === null ? 'NULL' : typeof v === 'number' ? String(v) : `'${v.replace(/'/g, "''")}'`;

/**
 * Setup statements seeding the 16-row qc_fixture data into `tableName`
 * (+ injected `__row__` BIGINT, 0-based file order). With `dataView` the
 * canonical view `data` is created over it — all rule SQL targets `data`
 * (architecture.md §4). runQC tests seed `quac_typed` WITHOUT the view:
 * the engine's prepare stage owns `quac_work` + `data` (engine spec §3).
 */
export function qcFixtureSetupSql(
  tableName: string,
  opts: { dataView?: boolean } = {},
): string[] {
  const values = QC_FIXTURE_ROWS.map(
    (row, i) => `(${[i, ...row].map(sqlLiteral).join(', ')})`,
  ).join(',\n');
  const statements = [
    `CREATE TABLE ${tableName} (
    __row__ BIGINT,
    record_id VARCHAR,
    household_id VARCHAR,
    wave INTEGER,
    panel_entry_wave INTEGER,
    baseline_record INTEGER,
    interview_date VARCHAR,
    reference_age INTEGER,
    reference_education INTEGER,
    household_size INTEGER,
    adult_count INTEGER,
    child_count INTEGER,
    tenure INTEGER,
    monthly_rent INTEGER,
    wage_income_annual INTEGER,
    selfemp_income_annual INTEGER,
    total_household_income_annual INTEGER,
    credit_card_balance INTEGER,
    partner_present INTEGER,
    marital_status INTEGER
  )`,
    `INSERT INTO ${tableName} VALUES\n${values}`,
  ];
  if (opts.dataView === true) {
    statements.push(`CREATE VIEW data AS SELECT * FROM ${tableName}`);
  }
  return statements;
}
